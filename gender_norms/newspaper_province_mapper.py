def map_newspaper_to_province(newspaper_name: str) -> str:
    """
    将报纸名称映射到省份
    
    Returns:
        省份名称、"全国"、"行业"、或"未知"
    """
    # 1. 检查是否为全国性报纸
    if newspaper_name in NATIONAL_NEWSPAPERS:
        return "全国"
    
    # 2. 检查是否为省级日报（如"北京日报"）
    for province in PROVINCES:
        if newspaper_name == f"{province}日报":
            return province
        if newspaper_name == f"{province}经济日报":
            return province
        if newspaper_name == f"{province}科技报":
            return province
        if newspaper_name == f"{province}法制报":
            return province
    
    # 3. 检查城市日报
    for city, province in CITY_TO_PROVINCE.items():
        if newspaper_name == f"{city}日报":
            return province
        if newspaper_name == f"{city}晚报":
            return province
        if newspaper_name == f"{city}商报":
            return province
        if newspaper_name == f"{city}时报":
            return province
        if newspaper_name == f"{city}特区报":
            return province
    
    # 4. 检查学报（大学）
    for university, province in UNIVERSITY_TO_PROVINCE.items():
        if university in newspaper_name and "学报" in newspaper_name:
            return province
    
    # 5. 检查城市名在报纸名中
    for city, province in CITY_TO_PROVINCE.items():
        if city in newspaper_name:
            return province
    
    # 6. 检查省份名在报纸名中
    for province in PROVINCES:
        if province in newspaper_name:
            return province
    
    # 7. 特殊处理
    if "日报" in newspaper_name or "晚报" in newspaper_name:
        # 尝试提取城市名
        for city in sorted(CITY_TO_PROVINCE.keys(), key=len, reverse=True):
            if city in newspaper_name:
                return CITY_TO_PROVINCE[city]
    
    # 8. 行业报纸（包含"报"、"刊"但无法识别）
    if any(keyword in newspaper_name for keyword in ["报", "刊", "通讯"]):
        # 排除明显的地方报纸
        if "西藏" in newspaper_name or "拉萨" in newspaper_name:
            return "西藏"
        if "兵团" in newspaper_name:
            return "新疆"
        return "行业"
    
    # 9. 特殊处理一些容易误判的报纸
    special_cases = {
        "南方日报": "广东",  # 广东省委机关报
        "文汇报": "上海",  # 上海主要报纸
        "新华日报": "江苏",  # 江苏省委机关报
        "西江日报": "广东",  # 广东云浮
        "太行日报": "山西",  # 山西晋城
        "太行报": "山西",
        "联合日报": "山东",  # 山东政协
        "法制生活报": "贵州",  # 贵州
        "北方法制报": "黑龙江",
        "西部法制报": "陕西",
        "民主与法制": "全国",
        "民主与法制时报": "全国",
        "人民铁道": "全国",
        "人民邮电": "全国",
        "新清华": "全国",  # 清华大学
        "人民武警": "全国",
        "战士报": "全国",  # 军队报纸
        "友报": "全国",  # 辽宁政协
    }
    
    if newspaper_name in special_cases:
        return special_cases[newspaper_name]
    
    # 10. 无法识别
    return "未知"


def create_mapping():
    """创建报纸-省份映射"""
    # 读取报纸名称列表
    names_file = "gender_norms/newspaper_data/newspaper_names.txt"
    stats_file = "gender_norms/newspaper_data/newspaper_stats.csv"
    
    # 读取统计信息
    stats_df = pd.read_csv(stats_file, encoding='utf-8-sig')
    
    # 创建映射
    mapping = {}
    stats = {
        "total": 0,
        "province": 0,
        "national": 0,
        "industry": 0,
        "unknown": 0,
    }
    
    province_counts = {}
    
    for _, row in stats_df.iterrows():
        newspaper = row['报纸名称']
        article_count = row['文章数量']
        
        province = map_newspaper_to_province(newspaper)
        mapping[newspaper] = {
            "province": province,
            "article_count": article_count,
        }
        
        stats["total"] += 1
        if province in PROVINCES:
            stats["province"] += 1
            province_counts[province] = province_counts.get(province, 0) + article_count
        elif province == "全国":
            stats["national"] += 1
        elif province == "行业":
            stats["industry"] += 1
        else:
            stats["unknown"] += 1
    
    # 保存映射
    output_file = "gender_norms/newspaper_data/newspaper_province_mapping.json"
    with open(output_file, 'w', encoding='utf-8') as f:
        json.dump(mapping, f, ensure_ascii=False, indent=2)
    
    print(f"\n{'='*60}")
    print(f"报纸-省份映射完成")
    print(f"{'='*60}")
    print(f"总报纸数: {stats['total']}")
    print(f"省级报纸: {stats['province']}")
    print(f"全国性报纸: {stats['national']}")
    print(f"行业报纸: {stats['industry']}")
    print(f"未知: {stats['unknown']}")
    
    print(f"\n📊 各省份文章数统计（前10）:")
    sorted_provinces = sorted(province_counts.items(), key=lambda x: x[1], reverse=True)
    for province, count in sorted_provinces[:10]:
        print(f"  {province}: {count:,} 篇文章")
    
    print(f"\n💾 映射已保存: {output_file}")
    
    # 打印一些未识别的报纸（用于调试）
    unknown_newspapers = [
        name for name, info in mapping.items() 
        if info["province"] == "未知"
    ]
    if unknown_newspapers:
        print(f"\n⚠️  未识别的报纸（前20个）:")
        for name in unknown_newspapers[:20]:
            print(f"  - {name}")
    
    return mapping


if __name__ == "__main__":
    mapping = create_mapping()
